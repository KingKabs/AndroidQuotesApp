package com.sp.metapositivityquotes.other;

public class BookChaptersDataModel {
    private String chapter_id, chapter_title, chapter_description, chapter_content, access;

    public String getChapterID() {
        return chapter_id;
    }

    public void setChapterID(String chapter_id) {
        this.chapter_id = chapter_id;
    }

    public String getChapterTitle() {
        return chapter_title;
    }

    public void setChapterTitle(String chapter_title) {
        this.chapter_title = chapter_title;
    }


    public String getChapterDescription() {
        return chapter_description;
    }

    public void setChapterDescription(String chapter_description) {
        this.chapter_description = chapter_description;
    }

    public String getChapterContent() {
        return chapter_content;

    }

    public void setChapterContent(String chapter_content) {
        this.chapter_content = chapter_content;
    }

    public String getChapterAccess() {
        return access;
    }

    public void setChapterAccess(String access) {
        this.access = access;
    }


}
